package rgb565

type RGB565 uint16

const (
	// basic colors
	BLACK  RGB565 = 0b0
	GREEN  RGB565 = 0x07E0
	BLUE   RGB565 = 0x001F
	RED    RGB565 = 0xF800
	WHITE  RGB565 = RED | GREEN | BLUE
	YELLOW RGB565 = RED | GREEN

	// drived from RGB888
	FLORECENT_GREEN RGB565 = 0x1FE3
	LAWN_GREEN      RGB565 = 0x7FC0
	ROYAL_BLUE      RGB565 = 0x3B3B
	GHOST_WHITE     RGB565 = 0xF7BF
	CHARTREUSE      RGB565 = 0x7FE0
	PURPULE         RGB565 = 0x780F
	SILVER          RGB565 = 0xBDF7
)
